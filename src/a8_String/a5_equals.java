package a8_String;

public class a5_equals {

	public static void main(String[] args) {
		// equals
		
		String a="Aim";
		String b="Aim";
		
		System.out.println(a.equals(b));      //true
	
        String x="ASd";
        String y="ASD";
        System.out.println(x.equals(y));         //false
		
		
	}

}
