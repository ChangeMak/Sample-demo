package a1_variable;

public class static_variable {
       static int a=100;         //static variable 
       
       int b=200;                //non static variable 
       
	public static void main(String[] args) {
		
		static_variable abc=new static_variable();
		
		
        
		System.out.println(static_variable.a);      //call variable by using class name 
		
		
		//System.out.println(abc.a);         //call by using ref variable of object
	
		System.out.println(abc.b);
	}

}













































































































































































































































































