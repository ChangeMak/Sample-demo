package Task_cls;

public class T091_Alphabet {

	public static void main(String[] args) {

		char c;
		
		for(c ='A'; c<='Z'; c++) {                    // A=65 Z=90
			System.out.print(c +" ");
		}
		
		System.out.println();
		
		char d;
		for (d='a'; d<='z'; d++) {                    //a=97 b=122 
				System.out.print(d +" ");
		}
		
	}

}
